<script>
  export default {
    data() {
	  let self = this
      return {
        id: '',
        baseUrl: '',
        ro:{
				zhanghao : false,
				xingming : false,
				touxiang : false,
				chushengriqi : false,
				xueya : false,
				xuetang : false,
				xinlv : false,
				shenghuoxiguan : false,
				yiwangbingshi : false,
				manxingbing : false,
				jiankangzhuangkuang : false,
				dengjiriqi : false,
				hulizhanghao : false,
				hulixingming : false,
        },
        type: '',
        userTableName: localStorage.getItem('UserTableName'),
        ruleForm: {
          zhanghao: '',
          xingming: '',
          touxiang: '',
          chushengriqi: '',
          xueya: '',
          xuetang: '',
          xinlv: '',
          shenghuoxiguan: '',
          yiwangbingshi: '',
          manxingbing: '',
          jiankangzhuangkuang: '',
          dengjiriqi: '',
          hulizhanghao: '',
          hulixingming: '',
        },
        manxingbingOptions: [],
        jiankangzhuangkuangOptions: [],


        rules: {
          zhanghao: [
          ],
          xingming: [
          ],
          touxiang: [
          ],
          chushengriqi: [
          ],
          xueya: [
            { validator: this.$validate.isNumber, trigger: 'blur' },
          ],
          xuetang: [
            { validator: this.$validate.isNumber, trigger: 'blur' },
          ],
          xinlv: [
            { validator: this.$validate.isNumber, trigger: 'blur' },
          ],
          shenghuoxiguan: [
          ],
          yiwangbingshi: [
          ],
          manxingbing: [
          ],
          jiankangzhuangkuang: [
          ],
          dengjiriqi: [
          ],
          hulizhanghao: [
          ],
          hulixingming: [
          ],
        },
		centerType: false,
      };
    },
    computed: {



    },
    components: {
    },
    created() {
		if(this.$route.query.centerType){
			this.centerType = true
		}
	  //this.bg();
      let type = this.$route.query.type ? this.$route.query.type : '';
      this.init(type);
      this.baseUrl = this.$config.baseUrl;
      this.ruleForm.chushengriqi = this.getCurDate()
      this.ruleForm.dengjiriqi = this.getCurDate()
    },
    methods: {
      getMakeZero(s) {
          return s < 10 ? '0' + s : s;
      },
      // 下载
      download(file){
        window.open(`${file}`)
      },
      // 初始化
      init(type) {
        this.type = type;
        if(type=='cross'){
          var obj = JSON.parse(localStorage.getItem('crossObj'));
          for (var o in obj){
            if(o=='zhanghao'){
              this.ruleForm.zhanghao = obj[o];
              this.ro.zhanghao = true;
              continue;
            }
            if(o=='xingming'){
              this.ruleForm.xingming = obj[o];
              this.ro.xingming = true;
              continue;
            }
            if(o=='touxiang'){
              this.ruleForm.touxiang = obj[o].split(",")[0];
              this.ro.touxiang = true;
              continue;
            }
            if(o=='chushengriqi'){
              this.ruleForm.chushengriqi = obj[o];
              this.ro.chushengriqi = true;
              continue;
            }
            if(o=='xueya'){
              this.ruleForm.xueya = obj[o];
              this.ro.xueya = true;
              continue;
            }
            if(o=='xuetang'){
              this.ruleForm.xuetang = obj[o];
              this.ro.xuetang = true;
              continue;
            }
            if(o=='xinlv'){
              this.ruleForm.xinlv = obj[o];
              this.ro.xinlv = true;
              continue;
            }
            if(o=='shenghuoxiguan'){
              this.ruleForm.shenghuoxiguan = obj[o];
              this.ro.shenghuoxiguan = true;
              continue;
            }
            if(o=='yiwangbingshi'){
              this.ruleForm.yiwangbingshi = obj[o];
              this.ro.yiwangbingshi = true;
              continue;
            }
            if(o=='manxingbing'){
              this.ruleForm.manxingbing = obj[o];
              this.ro.manxingbing = true;
              continue;
            }
            if(o=='jiankangzhuangkuang'){
              this.ruleForm.jiankangzhuangkuang = obj[o];
              this.ro.jiankangzhuangkuang = true;
              continue;
            }
            if(o=='dengjiriqi'){
              this.ruleForm.dengjiriqi = obj[o];
              this.ro.dengjiriqi = true;
              continue;
            }
            if(o=='hulizhanghao'){
              this.ruleForm.hulizhanghao = obj[o];
              this.ro.hulizhanghao = true;
              continue;
            }
            if(o=='hulixingming'){
              this.ruleForm.hulixingming = obj[o];
              this.ro.hulixingming = true;
              continue;
            }
          }
        }else if(type=='edit'){
			this.info()
		}
        // 获取用户信息
        this.$http.get(this.userTableName + '/session', {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            var json = res.data.data;
            if((json.xingming!=''&&json.xingming) || json.xingming==0){
                this.ruleForm.xingming = json.xingming
            }
            if((json.hulizhanghao!=''&&json.hulizhanghao) || json.hulizhanghao==0){
                this.ruleForm.hulizhanghao = json.hulizhanghao
            }
            if((json.hulixingming!=''&&json.hulixingming) || json.hulixingming==0){
                this.ruleForm.hulixingming = json.hulixingming
            }
          }
        });
        this.manxingbingOptions = "有,无".split(',')
        this.jiankangzhuangkuangOptions = "健康,异常".split(',')

		if (localStorage.getItem('raffleType') && localStorage.getItem('raffleType') != null) {
			localStorage.removeItem('raffleType')
			setTimeout(() => {
				this.onSubmit()
			}, 300)
		}
      },

    // 多级联动参数
      // 多级联动参数
      info() {
        this.$http.get(`jiankangzhuangkuang/detail/${this.$route.query.id}`, {emulateJSON: true}).then(res => {
          if (res.data.code == 0) {
            this.ruleForm = res.data.data;
          }
        });
      },
      // 提交
      onSubmit() {

			//更新跨表属性
			var crossuserid;
			var crossrefid;
			var crossoptnum;
			this.$refs["ruleForm"].validate(valid => {
				if(valid) {
					if(this.type=='cross'){
						var statusColumnName = localStorage.getItem('statusColumnName');
						var statusColumnValue = localStorage.getItem('statusColumnValue');
						if(statusColumnName && statusColumnName!='') {
							var obj = JSON.parse(localStorage.getItem('crossObj'));
							if(!statusColumnName.startsWith("[")) {
								for (var o in obj){
									if(o==statusColumnName){
										obj[o] = statusColumnValue;
									}
								}
								var table = localStorage.getItem('crossTable');
								this.$http.post(table+'/update', obj).then(res => {});
							} else {
								crossuserid=Number(localStorage.getItem('frontUserid'));
								crossrefid=obj['id'];
								crossoptnum=localStorage.getItem('statusColumnName');
								crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
							}
						}
					}
					if(crossrefid && crossuserid) {
						this.ruleForm.crossuserid=crossuserid;
						this.ruleForm.crossrefid=crossrefid;
						var params = {
							page: 1,
							limit: 10,
							crossuserid:crossuserid,
							crossrefid:crossrefid,
						}
						this.$http.get('jiankangzhuangkuang/list', {
							params: params
						}).then(res => {
							if(res.data.data.total>=crossoptnum) {
								this.$message({
									message: localStorage.getItem('tips'),
									type: 'error',
									duration: 1500,
								});
								return false;
							} else {
								// 跨表计算


								this.$http.post(`jiankangzhuangkuang/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
									if (res.data.code == 0) {
										this.$message({
											message: '操作成功',
											type: 'success',
											duration: 1500,
											onClose: () => {
												this.$router.go(-1);
											}
										});
									} else {
										this.$message({
											message: res.data.msg,
											type: 'error',
											duration: 1500
										});
									}
								});
							}
						});
					} else {


						this.$http.post(`jiankangzhuangkuang/${this.ruleForm.id?'update':this.centerType?'save':'add'}`, this.ruleForm).then(res => {
							if (res.data.code == 0) {
								this.$message({
									message: '操作成功',
									type: 'success',
									duration: 1500,
									onClose: () => {
										this.$router.go(-1);
									}
								});
							} else {
								this.$message({
									message: res.data.msg,
									type: 'error',
									duration: 1500
								});
							}
						});
					}
				}
			});
		},
		// 获取uuid
		getUUID () {
			return new Date().getTime();
		},
		// 返回
		back() {
			this.$router.go(-1);
		},
      touxiangUploadChange(fileUrls) {
          this.ruleForm.touxiang = fileUrls.replace(new RegExp(this.$config.baseUrl,"g"),"");;
      },
    }
  };
</script>

